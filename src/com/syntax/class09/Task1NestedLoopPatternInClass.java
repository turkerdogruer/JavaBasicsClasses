package com.syntax.class09;

public class Task1NestedLoopPatternInClass {
	
	public static void main(String[] args) {
		
		/*Print the following pattern:
        *55555
        *4444
        *333
        *22
        *1 		
        */
		
		for(int x=5; x>0; x--) {
			
			for(int y=1; y<=x; y++) {
				
				System.out.print(x);
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
	}

}
