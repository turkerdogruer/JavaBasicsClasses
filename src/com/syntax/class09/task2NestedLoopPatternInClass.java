package com.syntax.class09;

public class task2NestedLoopPatternInClass {
	public static void main(String[] args) {
		
		
		for(int r=1; r<=5; r++) {
			
			for(int c=1; c<=r; c++) {
				
				System.out.print("*"+" ");
			}
			System.out.println();
		}
		
		for(int x=4; x>0; x--) {
			
			for(int y=1; y<=x;y++) {
				
				System.out.print("*"+" ");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
	}

}
