package com.syntax.class09;

public class taskTriangleNumbers {

	public static void main(String[] args) {
		// numbers triangle

		
		for(int x=1; x<6; x++) {
			
			for(int y=1; y<=x; y++) {
				
				System.out.print(y);
			}
			System.out.println();
		}
		
		
		System.out.println("====================");
		
		
		for(int b=1; b<6; b++) {
			
			for(int z=1; z<=b; z++) {
				
				System.out.print(b);
			}
			System.out.println();
		}
		
		
		
		
	}

}
