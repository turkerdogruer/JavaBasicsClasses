package com.syntax.class09;

public class NestedloopsPatternNumber {

	public static void main(String[] args) {

//rows == outer loop
		//column= inner loop
		
		for(int x=1; x<5; x++) {
			
			for(int y=1; y<6; y++) {
				
				System.out.print(y);
				
			}
			System.out.println();
		}
		
		
		
		
		
		
	}

}
